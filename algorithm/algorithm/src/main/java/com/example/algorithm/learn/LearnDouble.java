package com.example.algorithm.learn;

public class LearnDouble {
    public static void leDoubleDiv() {
        double a = 12d;
        float a1 = 12f;
        double b = 31d;
        float b1 = 31f;
        System.out.println(a / b1);
        System.out.println(a1 / b);
    }

    public static void main(String[] args) {
        leDoubleDiv();
    }
}
